const questions = [
    ...easyQuestions,
    ...mediumQuestions,
    ...hardQuestions
];


let index = 0;
let right = 0, wrong = 0;
let total = questions.length;
const questionBox = document.getElementById("que-box");
const optionInput = document.querySelectorAll(".options");

const loadQuestion = () => {
    if (index === total) {
        return endQuiz();
    } else {
        reset();
    }
    const data = questions[index];
    questionBox.innerText = `${index + 1}) ${data.question}`;
    optionInput[0].previousElementSibling.innerText = data.a;
    optionInput[1].previousElementSibling.innerText = data.b;
    optionInput[2].previousElementSibling.innerText = data.c;
    optionInput[3].previousElementSibling.innerText = data.d;
}


const submitQuiz = () => {
    const data = questions[index];
    const answer = getAnswer();
    if (answer === data.correct) {
        right++;
    } else {
        wrong++;
    }
    index++;
    loadQuestion();
    return;
}

const getAnswer = () => {
    let answer;
    optionInput.forEach((input) => {
        if (input.checked) {
            answer = input.value;
        }
    });
    return answer;
}

const reset = () => {
    optionInput.forEach((input) => {
        input.checked = false;
    });
}

const endQuiz = () => {
    document.getElementById("box").innerHTML = `
<h3> Thank you for playing the quiz </h3>
<h2> ${right} out of ${total} are correct ! </h2>
`
}


loadQuestion();

